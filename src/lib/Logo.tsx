export function Logo({ orientation = 'horizontal', color = 'original', height = 30, className }: LogoProps) {
  const textFill = color === 'original' ? BLUE : color === 'dark-blue' ? NAVY : 'white';
  const scale = height / 30;

  if (orientation === 'symbol') {
    const w = Math.round(45 * scale);
    const h = Math.round(30 * scale);
    return (
      <svg
        width={w}
        height={h}
        viewBox="0 0 45 30"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        className={className}
        aria-label="Airpals"
      >
        <SymbolSVG color={color} />
      </svg>
    );
  }

  // Horizontal: symbol (45×30) + gap (12) + text starting at x=55
  const w = Math.round(145 * scale);
  const h = Math.round(30 * scale);
  return (
    <svg
      width={w}
      height={h}
      viewBox="0 0 145 30"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      aria-label="Airpals"
    >
      <SymbolSVG color={color} />
      <TextPaths fill={textFill} />
    </svg>
  );
}